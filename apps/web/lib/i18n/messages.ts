export const messages = { en, fr } satisfies Record<Locale, Record<string, string>>;
export type MessageKey = keyof typeof en;

const interpolate = (text: string, vars?: Record<string, string | number>) => {
  if (!vars) return text;
  return Object.entries(vars).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), text);
};

export const translate = (locale: Locale, key: MessageKey, vars?: Record<string, string | number>) =>
  interpolate(messages[locale][key] ?? messages[defaultLocale][key] ?? key, vars);

/** Resolves an ICU-lite `key.one` / `key.other` pair based on English pluralization rules. */
export const translateCount = (locale: Locale, key: string, count: number, vars?: Record<string, string | number>) => {
  const pluralKey = `${key}.${count === 1 ? "one" : "other"}` as MessageKey;
  return interpolate(messages[locale][pluralKey] ?? String(count), { count, ...vars });
};
